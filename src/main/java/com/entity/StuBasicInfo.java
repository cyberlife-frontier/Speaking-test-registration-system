package com.entity;

public class StuBasicInfo {
	private String stu_ID_card;
	private String stu_ID_type;
	private String stu_gender;
	private String stu_name;
	private String stu_subject;
	
	public String getStu_ID_card() {
		return stu_ID_card;
	}
	public void setStu_ID_card(String stu_ID_card) {
		this.stu_ID_card = stu_ID_card;
	}
	public String getStu_gender() {
		return stu_gender;
	}
	public void setStu_gender(String stu_gender) {
		this.stu_gender = stu_gender;
	}
	public String getStu_ID_type() {
		return stu_ID_type;
	}
	public void setStu_ID_type(String stu_ID_type) {
		this.stu_ID_type = stu_ID_type;
	}
	public String getStu_name() {
		return stu_name;
	}
	public void setStu_name(String stu_name) {
		this.stu_name = stu_name;
	}
	public String getStu_subject() {
		return stu_subject;
	}
	public void setStu_subject(String stu_subject) {
		this.stu_subject = stu_subject;
	}
	
}
